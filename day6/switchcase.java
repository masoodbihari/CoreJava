package utility_1;

public class switchcase {
	public static void main(String args[]){
		int n = 15;
		switch(n) {
			case 1:
				System.out.println("1");
				break;
			case 13:
				System.out.println("13");
				break;
			case 2 :
				System.out.println("2");
				break;
			case 3 :
				System.out.println("3");
				break;
			default :
				System.out.println("default");
		}
	}
}
